package com.google.business.util.jpush;

public class Extra {

	private String messageId;
	private int urlFlag;

	public String getMessageId() {
		return messageId;
	}

	public Extra setMessageId(String messageId) {
		this.messageId = messageId;
		return this;
	}

	public int getUrlFlag() {
		return urlFlag;
	}

	public Extra setUrlFlag(int urlFlag) {
		this.urlFlag = urlFlag;
		return this;
	}

	

}
